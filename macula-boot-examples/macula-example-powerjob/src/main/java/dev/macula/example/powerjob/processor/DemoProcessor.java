package dev.macula.example.powerjob.processor;

import lombok.extern.slf4j.Slf4j;
import org.springframework.stereotype.Component;
import tech.powerjob.worker.core.processor.ProcessResult;
import tech.powerjob.worker.core.processor.TaskContext;
import tech.powerjob.worker.core.processor.sdk.BasicProcessor;
import tech.powerjob.worker.log.OmsLogger;

/**
 * {@code DemoProcessor} POWERJOB的任务处理示例
 *
 * @author rain
 * @since 2023/4/27 19:00
 */
@Component
@Slf4j
public class DemoProcessor implements BasicProcessor {
    @Override
    public ProcessResult process(TaskContext context) throws Exception {
        // 在线日志功能，可以直接在控制台查看任务日志，非常便捷
        OmsLogger omsLogger = context.getOmsLogger();
        omsLogger.info("BasicProcessorDemo start to process, current JobParams is {}.", context.getJobParams());

        log.info("basic processor demo, current Job Params is {}", context.getJobParams());

        return new ProcessResult(true, "result is hello world!");
    }
}
